#include <stdio.h>

/**
 * div - divide two integers
 * @a: first int
 * @b: second int
 * Return: division
 */
int div(int a, int b)
{
        return (a / b);
}	
/**
 * mod - mod two integers
 * @a: first int
 * @b: second int
 * Return: mode
 */
int mod(int a, int b)
{
        return (a % b);
}

/**
 * mul- multiply two integers
 * @a: first int
 * @b: second int
 * Return: multiplication
 */
int mul(int a, int b)
{
        return (a * b);
}

/**
 * sub -subract two integers
 * @a: first int
 * @b: second int
 * Return: subtraction
 */
int sub(int a, int b)
{
        return (a - b);
}	
